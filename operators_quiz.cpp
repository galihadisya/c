#include<stdio.h>


int main(){
	
	int angka1,angka2;
	printf("tambah\n");
	scanf("%d %d",&angka1, &angka2);
	printf("%d\n", angka1+angka2);
	
	printf("kurang\n");
	scanf("%d %d",&angka1, &angka2);
	printf("%d\n", angka1-angka2);
	
	printf("kali\n");
	scanf("%d %d",&angka1, &angka2);
	printf("%d\n", angka1*angka2);
	
	printf("bagi\n");
	scanf("%d %d",&angka1, &angka2);
	printf("%d\n", angka1/angka2);
	
	printf("modulus\n");
	scanf("%d %d",&angka1, &angka2);
	printf("%d\n", angka1%angka2);

return 0;
}

